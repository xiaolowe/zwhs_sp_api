package cn.org.citycloud.zwhs.bean;

import org.hibernate.validator.constraints.Length;

/**
 * 服务覆盖区域Bean
 * 
 * @author lanbo
 * @version [V1.0, 2016年1月26日]
 * @since [B2C/V1.0]
 */
public class SPCoverage
{
    // 省
    @Length(min = 6)
    private int regionProv;
    
    // 市
    @Length(min = 6)
    private int regionCity;
    
    // 区县
    @Length(min = 6)
    private int regionArea;
    
    /**
     * 获取 regionProv
     * 
     * @return 返回 regionProv
     */
    public int getRegionProv()
    {
        return regionProv;
    }
    
    /**
     * 设置 regionProv
     * 
     * @param 对regionProv进行赋值
     */
    public void setRegionProv(int regionProv)
    {
        this.regionProv = regionProv;
    }
    
    /**
     * 获取 regionCity
     * 
     * @return 返回 regionCity
     */
    public int getRegionCity()
    {
        return regionCity;
    }
    
    /**
     * 设置 regionCity
     * 
     * @param 对regionCity进行赋值
     */
    public void setRegionCity(int regionCity)
    {
        this.regionCity = regionCity;
    }
    
    /**
     * 获取 regionArea
     * 
     * @return 返回 regionArea
     */
    public int getRegionArea()
    {
        return regionArea;
    }
    
    /**
     * 设置 regionArea
     * 
     * @param 对regionArea进行赋值
     */
    public void setRegionArea(int regionArea)
    {
        this.regionArea = regionArea;
    }
    
}
